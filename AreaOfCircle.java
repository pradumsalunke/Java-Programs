//Write a java program to find the area of a circle who's radius is 2.5cm.

class AreaOfCircle
{
    public static void main(String[] args)
    {
         double radius = 2.5;
         final double pi = 3.14;
         double area = pi*(radius*radius);
         System.out.println("The area of circle is -"+area +"who's radius is"+radius);
     }
}
    
    