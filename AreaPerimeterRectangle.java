class AreaPerimeterRectangle
{
	public static void main(String[] args)
	{
		double width = 4.5;
	    double height = 7.9;
	    double area = width*height;
	    double perimeter = 2*(width+height);
	    System.out.println("Area of rectangle is "+area +" having height "+height +" and width "+width);
	    System.out.println("Perimeter of rectangle "+perimeter +" having height "+height +" and width "+width);
	}
	

}